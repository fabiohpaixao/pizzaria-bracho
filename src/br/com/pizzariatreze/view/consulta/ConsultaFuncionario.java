    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Integer id = (Integer) jTableFuncionario.getValueAt(jTableFuncionario.getSelectedRow(),0);
        if(funcionarioCtrl.delete(id)){
            atualizar(funcionarioCtrl.listar());
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(null, "N√£o foi poss√≠vel excluir o funcion√°rio.\nTente novamente.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

        public void atualizar(List<Object> lista) {
        try {
            /* Captura o modelo da tabela */
            FuncionarioTableModel modelo = (FuncionarioTableModel) jTableFuncionario.getModel();
            
            /* Copia os dados da consulta para a tabela */
            modelo.adicionar(lista);

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao tentar buscar um Funcionario");
        }
    }
